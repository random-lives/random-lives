def _assign_subregion(country, year, subregion):
    if country == 'China':
        # Core provinces — major dynasty assignments
        if subregion in CHINA_CORE:
            for start, end, polity in CHINA_CORE_DYNASTIES:
                if start <= year < end:
                    return polity
        # Southern Song for southern provinces
        if subregion in CHINA_SOUTH and 1127 <= year < 1279:
            return 'Southern Song Dynasty'
        # Additional province-specific rules (Shaanxi, Hebei, Guangxi, Manchuria)
        if subregion in CHINA_ADDITIONAL:
            for start, end, polity in CHINA_ADDITIONAL[subregion]:
                if start <= year < end:
                    return polity

        # Pre-Qin: peripheral provinces NOT_RELEVANT
        if subregion in CHINA_PRE_QIN_NOT_RELEVANT:
            if year < CHINA_PRE_QIN_NOT_RELEVANT[subregion]:
                return 'not_relevant'

        # Pre-770 BC: Shang/Western Zhou heartland NOT_RELEVANT
        # (not enough evidence for reliable territorial assignment)
        if subregion in CHINA_PRE_770_NOT_RELEVANT and year < -770:
            return 'not_relevant'

        # Late Tang (755-880) for southern provinces (Tang maintained control
        # of the south even after An Lushan; Huang Chao Rebellion 880 ended this)
        CHINA_LATE_TANG_SOUTH = {
            'Sichuan', 'Zhejiang', 'Jiangsu', 'Anhui', 'Hubei', 'Hunan',
            'Jiangxi', 'Fujian', 'Shanghai', 'Chongqing',
            'Henan', 'Shandong', 'Shanxi',  # Tang maintained control of these northern cores too
        }
        if subregion in CHINA_LATE_TANG_SOUTH and 755 <= year < 880:
            return 'Tang Dynasty'
        # Guangdong/Hainan: Tang until Huang Chao sacked Guangzhou (879)
        if subregion in ('Guangdong', 'Hainan') and 755 <= year < 879:
            return 'Tang Dynasty'

        # NOT_RELEVANT periods for Chinese provinces
        # Universal NOT_RELEVANT (Three Kingdoms, Western Jin, Five Dynasties)
        for nr_start, nr_end in CHINA_UNIVERSAL_NR:
            if nr_start <= year < nr_end:
                return 'not_relevant'
        # Southern provinces: NOT_RELEVANT during 304-589 (all southern dynasties <100M)
        if subregion in CHINA_SOUTH_NR_304_589 and 304 <= year < 589:
            return 'not_relevant'
        # Northern provinces during 304-386 (pre-Northern Wei, all <100M)
        if subregion in CHINA_NORTHERN_WEI_PROVINCES and 304 <= year < 386:
            return 'not_relevant'
        # Northern provinces during 534-589 (post-Northern Wei successor states, all <100M)
        if subregion in CHINA_NORTHERN_WEI_PROVINCES and 534 <= year < 589:
            return 'not_relevant'
        # Manchuria/frontier provinces: NOT_RELEVANT for most periods
        if subregion in ('Jilin', 'Heilongjiang') and 220 <= year < 1115:
            # No polity controlling Manchuria exceeded 100M before the Jurchen Jin
            return 'not_relevant'
        if subregion == 'Yunnan' and 220 <= year < 1253:
            # Nanzhao/Dali and predecessors all <100M; Yuan conquest 1253
            return 'not_relevant'

    elif country == 'Russia':
        # Tatarstan: Russian Empire from 1557 (after Kazan conquest consolidated)
        if subregion == 'Tatarstan, Respublika' and 1557 <= year < 1917:
            return 'Russian Empire'
        # Chelyabinsk: Russian Empire from 1740 (southern Urals fort line established)
        if subregion == "Chelyabinskaya oblast'" and 1740 <= year < 1917:
            return 'Russian Empire'

    elif country == 'India':
        # Named polity assignments
        if subregion in INDIA_SUBREGION:
            for start, end, polity in INDIA_SUBREGION[subregion]:
                if start <= year < end:
                    return polity
        # Check if before any polities existed in this sub-region
        if subregion in INDIA_SUBREGION_NO_POLITY_BEFORE:
            if year < INDIA_SUBREGION_NO_POLITY_BEFORE[subregion]:
                return 'no_known_polities'
        # Check NOT_RELEVANT periods
        if subregion in INDIA_SUBREGION_NR:
            for start, end in INDIA_SUBREGION_NR[subregion]:
                if start <= year < end:
                    return 'not_relevant'

    elif country == 'Pakistan':
        # Check NOT_RELEVANT periods
        if subregion in PAKISTAN_SUBREGION_NR:
            for start, end in PAKISTAN_SUBREGION_NR[subregion]:
                if start <= year < end:
                    return 'not_relevant'

    elif country == 'France':
        # Alsace-Lorraine gap 1871-1918: assign French Third Republic to all
        # departments EXCEPT the annexed ones (Bas-Rhin, Haut-Rhin, Moselle)
        ALSACE_LORRAINE = {'Bas-Rhin', 'Haut-Rhin', 'Moselle'}
        if 1871 <= year < 1918 and subregion and subregion not in ALSACE_LORRAINE:
            return 'French Third Republic'

    elif country == 'Germany':
        # BRD/DDR split 1949-1990
        DDR_STATES = {
            'Sachsen', 'Sachsen-Anhalt', 'Thüringen', 'Brandenburg',
            'Mecklenburg-Vorpommern',
        }
        if 1949 <= year < 1990:
            if subregion in DDR_STATES:
                return 'German Democratic Republic'
            elif subregion == 'Berlin':
                return 'indeterminate'  # Berlin was split
            elif subregion:
                return 'Federal Republic of Germany'
        # Allied occupation 1945-1949: all Germany was occupied
        # Soviet zone became DDR, Western zones became BRD
        # Leave as indeterminate for now

    elif country == 'Bangladesh':
        # East India Company controlled Bengal from 1765 (Diwani)
        # Chittagong was already part of Bengal by then (Mughal from 1666)
        if 1765 <= year < 1858:
            return 'East India Company'

    elif country == 'United Kingdom':
        if subregion in UK_SUBREGION_ASSIGNMENTS:
            for start, end, polity in UK_SUBREGION_ASSIGNMENTS[subregion]:
                if start <= year < end:
                    return polity

    return None
